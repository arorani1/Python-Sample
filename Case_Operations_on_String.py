'''Case Operations on a string in Python'''
str1="Python scripting"
print(str1.lower())
print(str1.upper())
print(str1)
#dir(variable) lists out all the operations, which can be performed on a String.
print(dir(str1))
print(str1.swapcase())
'''Title gets 1st character of each word in the string to upper case.'''
print(str1.title())
'''Capitalize gets 1st character of string in upper case and rest all in lower case'''
print(str1.capitalize())
