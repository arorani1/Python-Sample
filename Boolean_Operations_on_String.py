#Boolean operations on a string. It can be useful during conditional statements
str1="Python"
print(str1)
#To check if all the characters in a string are in lower case. (Result: True/False)
print(str1.islower())
#To check if the starting character of a string starts with P
print(str1.startswith("P"))
#To check if the starting character of a string ends with hon
print(str1.endswith("hon"))
#To check if a string is just a space.
str2=" "
print(str2.isspace())
#To check if all the characters in a string is alphabet
print(str1.isalpha())
