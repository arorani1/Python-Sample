#Define Tuples
empty_tuple=()
my_tuple=(1,2,3,9,4)
print(empty_tuple)
print(my_tuple)

mytuple1=(1,3,[6,4],5)
print(mytuple1)

'''Taking list from a tuple'''
print(mytuple1[2])
'''Taking data from the list in tuple'''
print(mytuple1[2][1])

#Note: A tuple once assigned, its data cannot be changed. Example: mytuple[0]=34 will throw an error
