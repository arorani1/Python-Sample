'''
To move a text to next line instead of using 'print' multiple times, we use '\n' (newline).
'''

print("Welcome to Python.\nWe are working with special characters.")

'''Special Characters are always kept in quotes (single or double).
\n -> newline
\b -> backspace (removes space between 2 words)
\t -> add tab space between words.
\ -> Escape Symbol
\a -> Alert sound'''
print("Hello World")
print("Hello \tWorld")
print("Hello \"World\"")
